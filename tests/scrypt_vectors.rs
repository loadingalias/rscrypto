/// scrypt has no streaming API (the memory-hard ROMix phase must see the full
/// input block up-front), so the "streaming equivalence" contract that other
/// primitives carry is simply: repeated invocations with identical inputs
/// must produce bit-identical output — i.e. no hidden global state, no
/// allocator sensitivity, no non-determinism in the kernel dispatch.
#[test]
fn scrypt_hash_is_idempotent() {
  let first = hash_64(b"same-password", b"same-salt-16-bytes", 4, 1, 1);
  let second = hash_64(b"same-password", b"same-salt-16-bytes", 4, 1, 1);
  let third = hash_64(b"same-password", b"same-salt-16-bytes", 4, 1, 1);
  assert_eq!(first, second);
  assert_eq!(second, third);
}
